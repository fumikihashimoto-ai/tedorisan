/** テーブルデータ → HTML変換 */
function tableToHtml(headers: string[], data: string[][]): string {
  const ths = headers.map((h) => `<th>${h}</th>`).join('');
  const rows = data
    .map((row) => {
      const tds = row.map((cell) => `<td>${cell}</td>`).join('');
      return `<tr>${tds}</tr>`;
    })
    .join('');
  return `<table><thead><tr>${ths}</tr></thead><tbody>${rows}</tbody></table>`;
}

/** リストデータ（label + content）→ HTML変換 */
function listToHtml(
  items: { label: string; content: string }[]
): string {
  const lis = items
    .map((item) => `<li><strong>${item.label}</strong>：${item.content}</li>`)
    .join('');
  return `<ul>${lis}</ul>`;
}

/** richTextBlock を生成 */
function richText(html: string) {
  return { fieldId: 'richTextBlock', richText: html };
}

/** partsBlock を生成（pointVariant は必須） */
function partsBlock(
  partType: string,
  extra?: Record<string, unknown>
) {
  return {
    fieldId: 'partsBlock',
    partType: [partType],
    pointVariant: [],
    ...extra,
  };
}

